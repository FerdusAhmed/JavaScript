let month = 5;
switch (month)
{
    case 1:
        case 2:
            console.log("winter");
            break;
    case 3:
        case 4:
         console.log("spring");
            break;   
    case 5:
        case 6:
            console.log("Summer");
            break;
    case 7:
        case 8:
         console.log("Monsoon");
            break;
    case 9:
        case 10:
            console.log("Autumn");
            break;
    default:
         console.log("Late Autumn");
            break;
}
